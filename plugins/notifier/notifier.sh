# D : Display a notification on song change.

export PLUG_NOTIFIER_VERSION="0.1"

__plug_notifier_notify() {
  case $1 in
    add              ) notify-send -u low -i "$SMPCP_ICON" "[smpcp]" "Now adding new songs..." ;;
    play|pause|change) notify-send -i "$(get_albumart)" "$(status)" ;;
    stop             ) notify-send -i "$SMPCP_ICON" " stopped."
  esac
}

